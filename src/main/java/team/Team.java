package team;

public class Team {

	private int teamID;
	private String memberName;
	private String memberIntro;
	private String IDPhotoName;
	private String IDPhotoRealName;
	
	public int getTeamID() {
		return teamID;
	}
	public void setTeamID(int teamID) {
		this.teamID = teamID;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberIntro() {
		return memberIntro;
	}
	public void setMemberIntro(String memberIntro) {
		this.memberIntro = memberIntro;
	}
	public String getIDPhotoName() {
		return IDPhotoName;
	}
	public void setIDPhotoName(String iDPhotoName) {
		IDPhotoName = iDPhotoName;
	}
	public String getIDPhotoRealName() {
		return IDPhotoRealName;
	}
	public void setIDPhotoRealName(String iDPhotoRealName) {
		IDPhotoRealName = iDPhotoRealName;
	}
	
}
